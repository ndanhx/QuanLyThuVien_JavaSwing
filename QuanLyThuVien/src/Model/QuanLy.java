
package Model;

/**
 *
 * @author Duy Anh
 */
public class QuanLy {
    
    private String maQuanLy = "";
    private String passWordQuanLy = "";
    private String tenQuanLy = "";
    private String ngaySinh = "";
    private String gioiTinh = "";
    private String email = "";

    public QuanLy() {
    }

    
    public String getMaQuanLy() {
        return maQuanLy;
    }

    public void setMaQuanLy(String maQuanLy) {
        this.maQuanLy = maQuanLy;
    }

    public String getPassWordQuanLy() {
        return passWordQuanLy;
    }

    public void setPassWordQuanLy(String passWordQuanLy) {
        this.passWordQuanLy = passWordQuanLy;
    }

    public String getTenQuanLy() {
        return tenQuanLy;
    }

    public void setTenQuanLy(String tenQuanLy) {
        this.tenQuanLy = tenQuanLy;
    }

    public String getNgaySinh() {
        return ngaySinh;
    }

    public void setNgaySinh(String ngaySinh) {
        this.ngaySinh = ngaySinh;
    }

    public String getGioiTinh() {
        return gioiTinh;
    }

    public void setGioiTinh(String gioiTinh) {
        this.gioiTinh = gioiTinh;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
    
    
}
